package DesignPattern.src.main.java.com.example.DesignPatterns;

import java.util.List;

public class SubwaySub {
    String bread;
    List<String> sauses;
    List<String> vegies;

    public SubwaySub(String bread, List<String> sauses, List<String> vegies) {
        this.bread = bread;
        this.sauses = sauses;
        this.vegies = vegies;
    }
}
